using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Web;
using System.Web.Mvc;

namespace classlar.Classes
{
    public class Vendor
    {
        public Vendor()
        {
            VendorId = Guid.NewGuid().ToString();
        }
        [Key]
        [HiddenInput(DisplayValue = false)]
        public string VendorId { get; set; }
        [Display(Name = "Vendor Name")]
        [Required(ErrorMessage = "Please Enter Vendor Name")]
        public string VendorName { get; set; }
        [Display(Name = "Vendor Address")]
        [Required(ErrorMessage = "Please Enter Vendor Address")]
        public string VendorAddress { get; set; }
        [Display(Name = "Vendor Email")]
        [Required(ErrorMessage = "Please Enter Vendor Email")]
        public string VendorEmail { get; set; }
    }
}